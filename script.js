// Genera il catalogo con Bootstrap Cards
function generaCatalogo() {
  const catalogoElement = document.getElementById("catalogo");

  for (let modello in modelli) {
    const modelloData = modelli[modello];

    const cardDiv = document.createElement("div");
    cardDiv.classList.add("col-md-4", "mb-4");

    cardDiv.innerHTML = `
      <div class="card h-100 ingrandita" data-modello="${modello}" style="cursor: pointer;">
        <img src="${modelloData.img}" class="card-img-top" alt="${modelloData.nomeModello}">
        <div class="card-body">
          <h5 class="card-title piccola">${modelloData.nomeModello}</h5>
          <p class="card-text piccola">${modelloData.descrizione}</p>
        </div>
      </div>
    `;

    const cardElement = cardDiv.querySelector(".card");

    cardElement.addEventListener("click", function () {
      if (modello === "jordan-1") {
        // Solo la Jordan 1 porta al dettaglio
        window.location.href = `model-detail.html?modello=${modello}`;
      } else {
      }
    });

    catalogoElement.appendChild(cardDiv);
  }
}

// Esegue al caricamento della pagina
window.onload = function () {
  if (document.getElementById("catalogo")) {
    generaCatalogo();
  }
};
